    private void SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveActionPerformed
        // TODO add your handling code here:
        String roll=rollno.getText();
        String se=dsa.getText();
        String sm=os.getText();
        String em=oops.getText();
        String ie=cn.getText();

        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/portal","root","Abhi@123");

            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select * from student where rollno='"+roll+"'");

            if(rs.next()){
                st.executeUpdate("insert into btech_civil(rollno,se,sm,em,ie) values('"+roll+"','"+se+"','"+sm+"','"+em+"','"+ie+"') ");
                JOptionPane.showMessageDialog(null, "Data Updated Successfully");
                setVisible(false);
                new Civil().setVisible(true);
            }else{
                JOptionPane.showMessageDialog(null, "Enter Valid Roll Number");
            }

        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_SaveActionPerformed


    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        try{
            
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/portal","root","Abhi@123");

            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select * from student inner join btech_cs where student.rollno='"+roll+"' and btech_cs.rollno='"+roll+"'");
            
            if(rs.next()){//if condition is important before getting string from rs
                rollText.setText(roll);
                name.setText(rs.getString(4));
                course.setText(rs.getString(2));
                branch.setText(rs.getString(3));
                
                dsa.setText(rs.getString(8));
                oops.setText(rs.getString(9));
                os.setText(rs.getString(10));
                cn.setText(rs.getString(11));
                
                int s1=Integer.parseInt(dsa.getText());
                int s2=Integer.parseInt(oops.getText());
                int s3=Integer.parseInt(os.getText());
                int s4=Integer.parseInt(cn.getText());
                
                int sum=s1+s2+s3+s4;
                total.setText(sum+"");
                if(sum>=140){
                    pass.setText("PASS");
                }else{
                    pass.setText("FAIL");
                }
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_formComponentShown


    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        // TODO add your handling code here:
        String roll=rollno.getText();
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/portal","root","Abhi@123");

            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select * from btech_civil where rollno='"+roll+"'");
            if(rs.next()){
                setVisible(false);
                new Civil_Result_Data(roll).setVisible(true);
            }else{
                JOptionPane.showMessageDialog(null, "Result is not upload.");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_searchActionPerformed


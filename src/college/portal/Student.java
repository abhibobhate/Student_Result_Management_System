    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        // TODO add your handling code here:
        String dep=(String)(department.getSelectedItem());
        
        try{
            setVisible(false);
            switch (dep) {
                case "CS":new CS_Result().setVisible(true);                   
                    break;
                case "IT":new IT_Result().setVisible(true);                   
                    break;
                case "Electronics":new Electronics_Result().setVisible(true);                   
                    break;
                case "Civil":new Civil_Result().setVisible(true);                   
                    break;     
                default:
                    new Maths_Result().setVisible(true);                   
                    break;
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_searchActionPerformed


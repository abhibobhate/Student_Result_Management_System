    private void SubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitActionPerformed
        // TODO add your handling code here:
        String roll=rollno.getText();
        String course1=(String)(CourseName.getSelectedItem());
        
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/portal","root","Abhi@123");
           
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select * from student where rollno='"+roll+"' and course='"+course1+"' ");
            if(rs.next()){
                setVisible(false);
                switch (course1) {
                    case "B.Tech":new Btech().setVisible(true);                      
                        break;
                    case "M.Tech":new Mtech().setVisible(true);                      
                        break;
                    case "BCA":new BCA().setVisible(true);                      
                        break;
                    case "MCA":new MCA().setVisible(true);                      
                        break;    
                    default:
                        new MSC().setVisible(true);
                        break;
                }
            }else{
                JOptionPane.showMessageDialog(null, "Roll Number does not exists");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_SubmitActionPerformed

